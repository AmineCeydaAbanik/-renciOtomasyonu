    public EditMidtermGradePage( JFrame mainFrame, Teacher teacher) {
        initComponents();
        
        this.mainFrame = mainFrame;
        this.teacher = teacher;
        tableModel = new DefaultTableModel(0, 0);

        // add header of the table
        String header[] = new String[] { "id", "not" };
   
        tableModel.setColumnIdentifiers(header);
        jTable1.setModel(tableModel);
        
        // get all students from the db using student object.
        Student student = new Student();
        students = student.getAllStudents();
        
        // get all midterm grades of the students from the db.
        TakenMidtermGrades grade = new TakenMidtermGrades();
        grades = grade.getAllMidtermGrades();
        
        // add students to the model with their grade one by one.
        for( int i = 0; i < students.size(); i++ ){
            
            tableModel.addRow(new Object[] { students.get(i).getId(), grades[students.get(i).getId()].getGrade()});
        }
        // if a grade is edited, then update it in the list. 
        tableModel.addTableModelListener( new TableModelListener(){
            @Override
            public void tableChanged( TableModelEvent e){
                int col = jTable1.getSelectedColumn();
                int row = jTable1.getSelectedRow();
                
                if( col == 1){
                    float grade = Float.parseFloat( ( String) tableModel.getValueAt(row, col) );
                    // update the grade in the db.
                    TakenMidtermGrades gradeObject = new TakenMidtermGrades( students.get(row).getId(), teacher.getId(), grade);
                    gradeObject.addToDb();
                }
            }
        });
    }
 

       
